package virtualAgenda.model;

/**
 *
 * @author FELIPECANTINI
 */
public class Agenda {
    
    private int id;
    private int idUser;
    private String name;
    private String hour;
    private String date;

    public Agenda(int idUser, String name, String hour, String date) {
        this.idUser = idUser;
        this.name = name;
        this.hour = hour;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdUser() {
        return idUser;
    }

    public void setIdUser(int idUser) {
        this.idUser = idUser;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHour() {
        return hour;
    }

    public void setHour(String hour) {
        this.hour = hour;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
   
}
